    private void btnRealizarTesteLSIIndividual_TLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRealizarTesteLSIIndividual_TLActionPerformed
        if ((txtBG_CODIGO_TL.getText().trim().isEmpty()))
        {
            JOptionPane.showMessageDialog(null, "Você precisa informar um BUG!", "Alerta", JOptionPane.CLOSED_OPTION);
            btnSelecionaBug_TL.requestFocus();
            return;
        }

        if ((txtTL_PESQUISA_TL.getText().trim().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Você precisa informar uma pesquisa válida!", "Alerta", JOptionPane.CLOSED_OPTION);
            txtTL_PESQUISA_TL.requestFocus();
            return;
        }

        if ((JOptionPane.showConfirmDialog(null, "Confirma teste individual?", "Confirmação", JOptionPane.YES_NO_OPTION)) != 0)
        {
            return;
        }

        Stopwords Stopwords = new Stopwords();
        Stopwords.geraStopwords(txtTL_PESQUISA_TL.getText().trim());
        Stopwords = null;


        testeLSI testeLSI = new testeLSI();

        testeLSI.setBG_CODIGO(Integer.parseInt(txtBG_CODIGO_TL.getText()));

        testeLSI.setTL_PESQUISA(txtTL_PESQUISA_TL.getText());

        testeLSI.setTL_VALORK(Integer.parseInt(cbxTL_VALORK_TL.getSelectedItem().toString()));

        testeLSI.setTL_PESOMETODO(Integer.parseInt(cbxTL_PESOMETODO_TL.getSelectedItem().toString()));

        testeLSI.setTL_PESOFRAGMETODO(Integer.parseInt(cbxTL_PESOFRAGMETODO_TL.getSelectedItem().toString()));

        testeLSI.setTL_PESOCLASSE(Integer.parseInt(cbxTL_PESOCLASSE_TL.getSelectedItem().toString()));

        testeLSI.setTL_PESOFRAGCLASSE(Integer.parseInt(cbxTL_PESOFRAGCLASSE_TL.getSelectedItem().toString()));

        String temporario[] = (cbxTL_STOPWORDS_TL.getSelectedItem().toString()).split("-");

        testeLSI.setTL_STOPWORDS(Integer.parseInt(temporario[0].trim()));

        temporario = (cbxTL_CORPUS_TL.getSelectedItem().toString()).split("-");

        testeLSI.setTL_CORPUS(Integer.parseInt(temporario[0].trim()));


        testeLSI.imprimeHora();
        System.out.println("Inicializando o teste");
        testeLSI.realizarTesteLSIIndividual();
        System.out.println("Finalizando o teste");
        testeLSI.imprimeHora();
        testeLSI = null;

    }//GEN-LAST:event_btnRealizarTesteLSIIndividual_TLActionPerformed

